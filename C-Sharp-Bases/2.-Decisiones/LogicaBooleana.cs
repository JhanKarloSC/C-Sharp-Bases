    internal class LogicaBooleana
    {
        public LogicaBooleana()
        {
            var numero1 = 10;
            var numero2 = 5;

            var numero1IgualCantidad2 = numero1 == numero2;
            var numero1DiferenteCantidad2 = numero1 != numero2;

            var numero1MayorNumero2 = numero1 > numero2;
            var numero1MenorNumero2 = numero1 < numero2;

            var numero1MayorIgualNumero2 = numero1 >= numero2;
            var numero1MenorIgualNumero2 = numero1 <= numero2;

        }
    }
